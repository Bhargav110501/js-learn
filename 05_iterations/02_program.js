// While loop

let index = 0
while( index <= 10){
    // console.log(`Value of index is ${index}`);
    index += 2
}
/*
Value of index is 0
Value of index is 2
Value of index is 4
Value of index is 6
Value of index is 8
Value of index is 10
*/

let myArray = ["falsh", "batman", "superman"]

let arr = 0
while (arr < myArray.length){
    // console.log(`Value is ${myArray[arr]}`);
    arr++
}
/*
Value is falsh
Value is batman
Value is superman
*/

// do - while

let score = 1
do{
    // console.log(`score is ${score}`);
    score++
}while(score <= 10)
/*
score is 1
score is 2
score is 3
score is 4
score is 5
score is 6
score is 7
score is 8
score is 9
score is 10
*/

